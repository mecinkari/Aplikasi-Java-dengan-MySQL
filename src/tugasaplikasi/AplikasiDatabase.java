    private void ambil_data_table() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            Connection conn = buat_koneksi();
            Statement s = conn.createStatement();
            String sql = "SELECT * FROM karyawan";
            ResultSet rs = s.executeQuery(sql);
            
            while (rs.next()) {                
                Object[] obj = new Object[4];
                obj[0] = rs.getString("id");
                obj[1] = rs.getString("nama");
                obj[2] = rs.getString("alamat");
                obj[3] = rs.getString("telepon");
                model.addRow(obj);
            }
            
            rs.close();
            s.close();
             ambil_table_click();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e);
        }
    }
    
    private void ambil_table_click() {
        int i = this.jTable1.getSelectedRow();
        
        if (i == -1) {
            return;
        }
        
        String id = (String) model.getValueAt(i, 0);
        this.LblKode.setText(id);
        String nama = (String) model.getValueAt(i, 1);
        this.TxtNama.setText(nama);
        String alamat = (String) model.getValueAt(i, 2);
        this.TxtAlamat.setText(alamat);
        String telepon = (String) model.getValueAt(i, 3);
        this.TxtTelepon.setText(telepon);
    }
    
