    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        // TODO add your handling code here:
        Connection conn = buat_koneksi();
        
        if("Add".equals(this.BtnAdd.getText())){
            this.BtnAdd.setText("Save");
            this.BtnEdit.setText("Cancel");
            this.BtnDelete.setEnabled(false);
            this.BtnRefresh.setEnabled(false);
            this.LblKode.setText("0");
            this.TxtNama.setText("");
            this.TxtAlamat.setText("");
            this.TxtTelepon.setText("");
        } else if ("Save".equals(this.BtnAdd.getText())) {
            String sql = "INSERT INTO karyawan (nama, alamat, telepon) values ('" + this.TxtNama.getText() + "', '" + this.TxtAlamat.getText() + "', '" + this.TxtTelepon.getText() + "')";
            
            try {
                PreparedStatement ps = (PreparedStatement) conn.prepareStatement(sql);
                ps.executeUpdate();
                ps.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e);
            }
            
            this.BtnAdd.setText("Add");
            this.BtnEdit.setText("Edit");
            this.BtnDelete.setEnabled(true);
            this.BtnRefresh.setEnabled(true);
        } else if ("Update".equals(this.BtnAdd.getText())) {
            String sql = "UPDATE karyawan SET nama = '" + this.TxtNama.getText() + "', alamat = '" + this.TxtAlamat.getText() + "', telepon = '" + this.TxtTelepon.getText() + "' WHERE id = " + this.LblKode.getText();
            
            try {
                PreparedStatement ps = (PreparedStatement) conn.prepareStatement(sql);
                ps.executeUpdate();
                ps.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e);
            }
            
            this.BtnAdd.setText("Add");
            this.BtnEdit.setText("Edit");
            this.BtnDelete.setEnabled(true);
            this.BtnRefresh.setEnabled(true);
        }
    }//GEN-LAST:event_BtnAddActionPerformed

